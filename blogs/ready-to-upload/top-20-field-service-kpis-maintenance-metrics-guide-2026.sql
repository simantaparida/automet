## Category 1: Technician Productivity KPIs

These metrics measure how efficiently your technicians are working.

---

### **KPI #1: First-Time Fix Rate (FTFR)**

**Definition:** Percentage of jobs resolved in the first visit (without requiring a second visit).

**Formula:**
```
FTFR = (Jobs completed in 1 visit / Total jobs) √ó 100
```

**Example:**
- 85 jobs completed in 1 visit
- 100 total jobs
- FTFR = (85 / 100) √ó 100 = **85%**

**Target:** 80-90%

**Why it matters:** Low FTFR means wasted time, fuel, and customer frustration. High FTFR indicates technicians arrive prepared with the right parts and skills.

**How to improve:**
- Ensure technicians review asset history before visiting
- Stock vans with common parts
- Improve initial diagnostics (remote troubleshooting before dispatch)
- Train technicians on complex issues

**Red flag:** FTFR <70% ‚Üí major issue (wrong parts inventory, poor diagnostics, or insufficient training)

---

### **KPI #2: Technician Utilization Rate**

**Definition:** Percentage of time technicians spend on billable work vs. total available work hours.

**Formula:**
```
Utilization Rate = (Billable hours / Total available hours) √ó 100
```

**Example:**
- Technician works 8 hours/day (40 hours/week)
- Billable work: 26 hours/week (actual on-site job time)
- Non-billable: 14 hours/week (travel, admin, breaks)
- Utilization = (26 / 40) √ó 100 = **65%**

**Target:** 60-75%

**Why it matters:** Too low (<50%) = wasted capacity. Too high (>80%) = technician burnout.

**How to improve:**
- Reduce travel time (route optimization, geographically cluster jobs)
- Minimize admin work (digital forms instead of paperwork)
- Pre-schedule jobs to avoid idle time

**Industry benchmark:**
- HVAC/plumbing: 60-70%
- IT field service: 50-60%
- Industrial maintenance: 65-75%

---

### **KPI #3: Jobs Completed Per Day**

**Definition:** Average number of jobs a technician completes in one workday.

**Formula:**
```
Jobs/Day = Total jobs completed / Total workdays
```

**Example:**
- 120 jobs completed in a month
- 24 workdays
- Jobs/Day = 120 / 24 = **5 jobs/day**

**Target:** 4-7 jobs/day (varies by industry and job complexity)

**Why it matters:** Direct measure of throughput. Higher = more efficient.

**Factors that affect this KPI:**
- Job complexity (HVAC repair: 1-2 hours; elevator inspection: 4 hours)
- Travel distance (urban: 5-7 jobs/day; rural: 3-4 jobs/day)
- Job clustering (consecutive jobs in same area vs. scattered)

**How to improve:**
- Schedule consecutive jobs in the same geographic zone
- Reduce job duration (better parts inventory, training)
- Minimize gaps between jobs (auto-scheduling)

---

### **KPI #4: Average Job Duration**

**Definition:** Average time from technician check-in to job completion.

**Formula:**
```
Avg Job Duration = Total job hours / Number of jobs
```

**Example:**
- 80 hours spent on jobs in a week
- 20 jobs completed
- Avg Duration = 80 / 20 = **4 hours/job**

**Target:** Varies by job type:
- HVAC repair: 60-90 minutes
- Electrical emergency: 90-120 minutes
- Preventive maintenance: 30-60 minutes
- Complex installations: 3-6 hours

**Why it matters:** Long durations indicate inefficiency, missing parts, or complex issues.

**How to improve:**
- Track duration by job type (not just overall average)
- Identify outliers (why did this PM take 3 hours when average is 45 minutes?)
- Compare technicians (why does Technician A average 60 min while Technician B averages 110 min?)

---

### **KPI #5: Travel Time vs. Work Time Ratio**

**Definition:** Percentage of time spent traveling to jobs vs. actually working on-site.

**Formula:**
```
Travel Ratio = (Travel time / Total time) √ó 100
```

**Example:**
- Travel time: 3 hours/day
- Work time: 5 hours/day
- Total: 8 hours/day
- Travel Ratio = (3 / 8) √ó 100 = **37.5%**

**Target:** <30%

**Why it matters:** High travel time = low productivity, high fuel costs.

**How to improve:**
- Geographic job clustering (all jobs in Zone A on Monday, Zone B on Tuesday)
- Route optimization software
- Reduce service radius (focus on profitable zones)

**Industry insight:** Global field service teams in dense urban areas achieve 20-25% travel time. Rural teams: 35-45%.

