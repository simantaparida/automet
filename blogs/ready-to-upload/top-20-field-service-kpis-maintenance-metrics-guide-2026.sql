**Target:** +30 to +50 (world-class: +70)

**Why it matters:** NPS predicts growth. High NPS = customers actively refer you. Low NPS (<0) = customers warn others away.

**Industry benchmarks:**
- HVAC/plumbing: +30 to +50
- IT field service: +20 to +40
- Industrial maintenance: +40 to +60

**How to improve:**
- Improve CSAT first (NPS follows CSAT)
