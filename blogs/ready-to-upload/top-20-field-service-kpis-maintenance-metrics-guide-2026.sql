### **KPI #12: Mean Time Between Failures (MTBF)**

**Definition:** Average time between equipment failures (measures asset reliability).

**Formula:**
```
MTBF = Total operational time / Number of failures
```

**Example:**
- Equipment ran for 1,800 hours
