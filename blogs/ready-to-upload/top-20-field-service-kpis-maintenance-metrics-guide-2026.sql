- MTBF = 1,800 / 10 = **180 hours**

**Target:** Varies by asset type:
- HVAC: 2,000-3,000 hours
- Elevators: 5,000-8,000 hours
- Manufacturing equipment: 1,500-2,500 hours

**Why it matters:** Higher MTBF = more reliable equipment, fewer breakdowns.

**How to improve:**
- Increase preventive maintenance frequency
- Replace aging assets (MTBF decreasing over time = end of life)
- Track failure patterns (same component failing repeatedly? Upgrade it.)

---

### **KPI #13: Mean Time to Repair (MTTR)**

**Definition:** Average time to repair equipment after a failure occurs.

**Formula:**
```
MTTR = Total repair time / Number of repairs
```

**Example:**
- 50 hours spent on repairs
- 10 repair jobs
- MTTR = 50 / 10 = **5 hours/repair**

**Target:** <4 hours (varies by industry)

**Why it matters:** Lower MTTR = less downtime, happier customers.

**How to improve:**
- Stock critical spare parts on-site or in technician vans
- Train technicians on common repairs
- Use digital asset history (technicians know what worked before)

**Related metric:** MTTR + MTBF = total asset availability.

---

### **KPI #14: Asset Uptime Percentage**

**Definition:** Percentage of time equipment is operational (not broken or under repair).

**Formula:**
```
