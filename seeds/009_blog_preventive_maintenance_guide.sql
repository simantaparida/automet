## What Is Preventive Maintenance? (Simple Definition)

**Preventive maintenance (PM) is the process of regularly inspecting, servicing, and maintaining equipment before failure happens ‚Äî based on time intervals, usage metrics, or condition indicators ‚Äî to prevent unexpected breakdowns and extend asset life.**

In simpler terms: **Don't wait for things to break. Maintain them regularly.**

### How PM Differs from Other Maintenance Strategies:

**Preventive Maintenance**
Scheduled maintenance performed at regular intervals (weekly, monthly, annually) or after specific usage (hours, cycles, mileage) ‚Äî regardless of current condition.

**Reactive Maintenance (Run-to-Failure)**
Fix equipment only after it breaks. No scheduled maintenance. High risk of downtime and safety incidents.

**Corrective Maintenance**
Identify and fix minor issues before they become major failures. Often discovered during inspections.

**Predictive Maintenance (PdM)**
Use real-time data from sensors (temperature, vibration, pressure) and analytics to predict when failure is likely. Maintenance is performed only when needed.

**Prescriptive Maintenance (AI-Driven)**
Advanced AI systems that not only predict failures but also recommend specific actions, optimal timing, and resource allocation.

**Key Distinction**: Preventive maintenance follows a fixed schedule. Predictive maintenance responds to real-time conditions. Both aim to prevent failures, but PM is simpler to implement and doesn't require advanced sensors or analytics.

