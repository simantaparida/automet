## What Should a Preventive Maintenance Checklist Include?

A good PM checklist ensures consistency and completeness. Here's what to include:

### 1. **Task Description**
Clear, specific instructions. "Inspect HVAC filter" is vague. "Replace HVAC filter if pressure drop exceeds 0.5 PSI" is precise.

### 2. **Tools & Parts Needed**
List everything required before starting. Prevents delays.

### 3. **Safety Instructions**
Lockout/tagout procedures, PPE requirements, hazard warnings.

### 4. **Step-by-Step Procedures**
Numbered steps in logical order. Reduces errors and training time.

### 5. **Acceptance Criteria**
How do you know the task is done correctly? "Filter seated properly with no gaps" or "Temperature reading within 20-25¬∞C."

### 6. **Photos/Proof Upload**
Require before/after images, meter readings, or condition documentation.

### 7. **Asset Readings**
Capture current values: operating hours, temperature, pressure, vibration levels.

### 8. **Technician Signature**
Digital sign-off confirms accountability.

### 9. **Next Due Date**
Calculate and display when this PM is due next.

